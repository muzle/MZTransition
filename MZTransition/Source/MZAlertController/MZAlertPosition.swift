import Foundation

/// `MZAlertPosition` - the presentation style of the MZAlertController
public enum MZAlertPosition: String, CaseIterable {
    /// `top` - presentation from screen top to bottom with view height offset
    case top
    /// `bottom` - presentation from screen bottom to top with view height offset
    case bottom
    /// `center` - presentation from bottom to center
    case center
}
